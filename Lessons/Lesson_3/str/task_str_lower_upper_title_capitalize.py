# Верхний и нижний регистр
text = 'однажды в СТУДЁНУЮ зИмнЮЮ ПоРУ'
print(text.upper())  # Все символы в верхний регистр
print(text.lower())  # Все символы в нижний регистр
print(text.title())  # Каждое слово начинается с заглавной буквы
print(text.capitalize()) # Каждая строка с заглавной буквы
